package CoreJava;

 class Sum {
	//method 1st with 2 arguments of same datatype:
	public int sum(int x, int y )
	{ int c= x+y;
	return c;}
	
	//method 2 
	public int  sum(int x,int y,int z)
	{ int d= x+y+z;
	return d;}
	
	//method 3
	public double sum(double x, double y)
	{ double e= x+y;
	return e;
	}

}



