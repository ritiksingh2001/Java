package CoreJava;

public class Pogo {
     //method with single parameter of type int 
	public void display(int n)
      {
    	  int i=0;
    	  while(i<10)
    	  {
    		  System.out.print(n*2);
    		  i++;
    	  }
      }
      //method with single parameter of type string 
      public void display(char b) {
      for(int i=0 ;i<10;i++)
      { System.out.print(b); }
      }
}

